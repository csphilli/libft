#include "libft.h"

int	ft_nbr_size(intmax_t nbr)
{
	int	len;

	if (nbr < 0)
		nbr *= -1;
	len = 1;
	while (nbr > 0)
	{
		nbr /= 10;
		len++;
	}
	return (len);
}
